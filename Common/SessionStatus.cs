namespace TgDataPlanner.Common;

/// <summary>
/// Статус сессии игровой группы.
/// Определяет текущее состояние процесса планирования и проведения сессии.
/// </summary>
public enum SessionStatus
{
    /// <summary>
    /// Сессия не назначена.
    /// Группа создана, но планирование ещё не начиналось.
    /// </summary>
    NoSession = 0,

    /// <summary>
    /// Ожидание ответов игроков (RSVP).
    /// Время сессии назначено, игроки подтверждают или отклоняют участие.
    /// </summary>
    Pending = 1,

    /// <summary>
    /// Сессия подтверждена.
    /// Набрано 75%+ игроков и ВСЕ администраторы могут присутствовать.
    /// </summary>
    Confirmed = 2,

    /// <summary>
    /// Сессия отменена.
    /// Недостаточно подтверждений от игроков (менее 75%).
    /// Требуется новый запрос свободного времени.
    /// </summary>
    Cancelled = 3,

    /// <summary>
    /// Требуется перепланирование.
    /// Администраторы не могут присутствовать в назначенное время.
    /// Запускается автоматический поиск нового времени.
    /// </summary>
    Rescheduled = 4
}
